package com.bayamp.training.calc;

public class Multiplication {
	
	public static int ConversionOfExchangeRate(int DollarAmount, int dollarRateForIndianRupee){
		
	int total = DollarAmount * dollarRateForIndianRupee;
	
	return total;
		
	}
	
	public double multiplicationOfGroups (int groupA , int groupB){
	
	double totalGroup = groupA * groupB;
	
	return totalGroup;
	
	}
	
	
}
